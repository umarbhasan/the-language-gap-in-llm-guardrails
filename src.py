def main():
    print("Parsing embedded text data...")
    lines = RAW_DATA.strip().split('\n')
    token_scores = []

    for line in lines:
        match = re.match(r'^(\d+)(.*?)(0\.\d.*)?$', line.strip())
        if not match:
            continue

        token_raw = match.group(2)
        nums_str = match.group(3)

        # Clean token presentation
        token = token_raw.replace("ฤ ", " ").replace("ฤ", "\n").replace("<|begin_of_text|>", "[BOS]")

        if nums_str:
            parts = nums_str.split('0.')
            vals = [float('0.' + p) for p in parts if p]
            if vals:
                # Get the highest attribution score for this token across all outputs
                max_score = max(vals)
                token_scores.append((token, max_score))

    # Sort descending by score
    token_scores.sort(key=lambda x: x[1], reverse=True)

    # Filter down to the top 50 unique tokens
    seen = set()
    unique_top_tokens = []
    for token, score in token_scores:
        if token not in seen:
            seen.add(token)
            unique_top_tokens.append((token, score))
            if len(unique_top_tokens) == 50:
                break

    output_csv = 'top_50_influential_tokens.csv'

    # Write directly to CSV
    print(f"Writing output to {output_csv}...")
    with open(output_csv, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        writer.writerow(['Token', 'Max_Attribution_Score'])
        writer.writerows(unique_top_tokens)

    print("Success! Data has been extracted successfully.")

