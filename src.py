    print("Parsing hardcoded attribution data...")
    lines = RAW_DATA.strip().split('\n')

    source_tokens = []
    attributions = []

    for line in lines:
        # Matches the line number, the token (letters/symbols), and the numerical string
        match = re.match(r'^(\d+)(.*?)(0\.\d.*)?$', line.strip())
        if not match: continue

        token_raw = match.group(2)
        nums_str = match.group(3)

        # Clean the token for the plot axis
        token = token_raw.replace("ฤ ", " ").replace("ฤ", "\n").replace("<|begin_of_text|>", "[BOS]")
        source_tokens.append(token)

        if nums_str:
            # We split by '0.' and mathematically reconstruct the floats
            parts = nums_str.split('0.')
            vals = [float('0.' + p) for p in parts if p]
        else:
            vals = []

        attributions.append(vals)

    # The generated sequence length is based on the first prompt token having values for all generated tokens
    N_cols = len(attributions[0])

    padded_matrix = []
    for vals in attributions:
        # Prepend NaNs to missing columns to visually form the causal masking triangle
        pad_len = N_cols - len(vals)
        row_padded = [np.nan] * pad_len + vals
        padded_matrix.append(row_padded)

    matrix = np.array(padded_matrix)

    # The columns represent the most recently generated tokens
    target_tokens = source_tokens[-N_cols:]

    print(f"Successfully reconstructed mathematical matrix with shape: {matrix.shape}")
