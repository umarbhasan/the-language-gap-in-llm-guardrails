def get_hidden_states(prompts, tokenizer, model):
    """Passes prompts through the model and extracts the final layer's hidden state."""
    hidden_states = []

    for prompt in prompts:
        chat = [{"role": "user", "content": prompt}]
        formatted_prompt = tokenizer.apply_chat_template(chat, tokenize=False, add_generation_prompt=True)
        inputs = tokenizer(formatted_prompt, return_tensors="pt").to(DEVICE)

        with torch.no_grad():
            # output_hidden_states=True forces the model to return its internal representations
            outputs = model(**inputs, output_hidden_states=True)

        # Extract the hidden states from the very last layer
        # Shape: (batch_size, sequence_length, hidden_dimension)
        last_layer = outputs.hidden_states[-1]

        # We take the representation of the very last token, which encapsulates the whole prompt context
        final_token_representation = last_layer[0, -1, :].cpu().numpy()
        hidden_states.append(final_token_representation)

    return np.array(hidden_states)

