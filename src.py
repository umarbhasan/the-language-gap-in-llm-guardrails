def translate_to_bengali(prompts):
    """
    Translates English prompts to Bengali using Meta's NLLB-200 model.
    """
    print("\nLoading NLLB-200 Translator...")
    model_name = "facebook/nllb-200-distilled-600M"
    tokenizer = AutoTokenizer.from_pretrained(model_name, src_lang="eng_Latn")
    model = AutoModelForSeq2SeqLM.from_pretrained(model_name).to(DEVICE)

    bengali_prompts = []
    print("Translating prompts to Bengali...")

    # NLLB code for Bengali is 'ben_Beng'
    tgt_lang_id = tokenizer.convert_tokens_to_ids("ben_Beng")

    for text in prompts:
        inputs = tokenizer(text, return_tensors="pt", truncation=True, max_length=400).to(DEVICE)

        with torch.no_grad():
            translated_tokens = model.generate(
                **inputs,
                forced_bos_token_id=tgt_lang_id,
                max_length=400
            )

        translated_text = tokenizer.batch_decode(translated_tokens, skip_special_tokens=True)[0]
        bengali_prompts.append(translated_text)

    # Free up memory
    del model
    del tokenizer
    torch.cuda.empty_cache()

    return bengali_prompts

