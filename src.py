def extract_top_tokens(html_path, output_csv, top_n=50):
    print(f"Reading HTML file: {html_path}")

    try:
        with open(html_path, 'r', encoding='utf-8') as file:
            soup = BeautifulSoup(file, 'html.parser')
    except FileNotFoundError:
        print(f"Error: Could not find '{html_path}'. Please check the file name.")
        return

    # Extract all text from the HTML, separating elements with line breaks
    raw_text = soup.get_text(separator='\n')

    token_scores = []

    # Regex to match the line number, the token, and the continuous string of floats
    pattern = re.compile(r'^(\d+)(.*?)(0\.\d.*)?$')

    for line in raw_text.split('\n'):
        match = pattern.match(line.strip())
        if not match:
            continue

        token_raw = match.group(2)
        nums_str = match.group(3)

        if not token_raw or not nums_str:
            continue

        # Clean the token representation
        token = token_raw.replace("ฤ ", " ").replace("ฤ", "\n").replace("<|begin_of_text|>", "[BOS]")

        # Split the conjoined floats (e.g., '0.1570.051') and reconstruct them
        parts = nums_str.split('0.')
        vals = [float('0.' + p) for p in parts if p]

        if vals:
            max_score = max(vals)
            token_scores.append((token, max_score))

    # Sort the tokens by their highest attribution score in descending order
    token_scores.sort(key=lambda x: x[1], reverse=True)

    # Deduplicate the list while preserving the sorted order
    seen = set()
    unique_top_tokens = []
    for token, score in token_scores:
        if token not in seen:
            seen.add(token)
            unique_top_tokens.append((token, score))
            if len(unique_top_tokens) == top_n:
                break

    # Export the results to a CSV file
    print(f"Writing top {len(unique_top_tokens)} tokens to {output_csv}")
    with open(output_csv, 'w', newline='', encoding='utf-8') as csv_file:
        writer = csv.writer(csv_file)
        writer.writerow(['Token', 'Max_Attribution_Score'])
        writer.writerows(unique_top_tokens)

    print("Extraction complete. The CSV is ready.")

