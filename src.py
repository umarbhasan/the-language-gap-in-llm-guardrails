def calculate_perplexity_and_length(prompts, tokenizer, model):
    """Calculates token length and generation perplexity for a list of prompts."""
    lengths = []
    perplexities = []

    for prompt in prompts:
        # Token Length Calculation
        inputs = tokenizer(prompt, return_tensors="pt")
        token_length = inputs.input_ids.shape[1]
        lengths.append(token_length)

        # Perplexity Calculation
        inputs = inputs.to(DEVICE)
        with torch.no_grad():
            # Providing labels=input_ids calculates the cross-entropy loss of the sequence
            outputs = model(**inputs, labels=inputs.input_ids)
            loss = outputs.loss.item()

            # Perplexity is the exponential of the cross-entropy loss
            try:
                ppl = math.exp(loss)
            except OverflowError:
                ppl = float('inf')

            perplexities.append(ppl)

    return lengths, perplexities

